function addition(a, b) {
  return a + b;
}

function difference(a, b) {
  return a - b;
}

function multiply(a, b) {
  return a * b;
}

function divide(a, b) {
  return a / b;
}

const arithmetics = {
  addition: addition(),
  difference: difference(),
  multiply: multiply(),
  divide: divide(),
};

export default arithmetics;
